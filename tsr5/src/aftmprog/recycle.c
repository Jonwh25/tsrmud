/* stuff for recycling bounties */
BOUNTY_DATA *bounty_free;

BOUNTY_DATA *new_bounty(void)
{
    static BOUNTY_DATA b_zero;
    BOUNTY_DATA *b;

    if (bounty_free == NULL)
	b = alloc_perm(sizeof(*b));
    else {
	b = bounty_free;
	bounty_free = bounty_free->next;
    }

    *b = b_zero;
    return b;
}

void free_bounty(BOUNTY_DATA * b)
{
    free_string(b->name);
    b->next = bounty_free;
    bounty_free = b;
}
