float mana_cost(CHAR_DATA* ch, int sn)
{
	float cost = 0;
	float skcost, sklevel;
	if (IS_NPC(ch))
		return cost;
	else
	{
		skcost = skill_table[sn].min_mana;
		sklevel = 100 / (2 + ch->level - skill_table[sn].skill_level[prime_class(ch)]);
		cost = UMAX (skcost, sklevel);
		return cost;
	}
	return cost;
}

float spell_cost(CHAR_DATA* ch, int sn)
{
	if (is_class(ch, CLASS_VAMPIRE))
	{
		return mana_cost(ch,sn) / 4;
	}
	else return mana_cost(ch,sn);
}
	
